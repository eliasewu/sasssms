/**
 * Zero-pad MNC to 3 digits for display.
 * "1" → "001", "02" → "002", "100" → "100"
 */
export function padMnc(mnc: string | null | undefined): string {
  if (!mnc) return "";
  return mnc.padStart(3, "0");
}

/**
 * Format MCC + MNC into combined 6-digit MCCMNC code.
 * 1-digit MNC → pad to "001", 2-digit → "020", 3-digit → "998"
 * Examples: (470, "1") → "470001", (412, "20") → "412020", (544, "998") → "544998"
 */
export function formatMccMnc(mcc: string, mnc: string): string {
  if (!mcc) return "";
  return mcc + (mnc || "").padStart(3, "0");
}

/**
 * Synchronous MCC lookup from a phone number (client-safe, no DB query).
 * Returns MCC from dial-code mapping and formatted 6-digit MCCMNC;
 * MNC requires server-side DB lookup.
 */
export function lookupMccSync(destination: string): { mcc: string; mnc: string; mccmnc: string } {
  const cleaned = destination.replace(/^\+/, "").replace(/[^0-9]/g, "");
  if (!cleaned || cleaned.length < 3) return { mcc: "", mnc: "", mccmnc: "" };

  const dialCodes = Object.keys(DIAL_TO_MCC).sort((a, b) => b.length - a.length);
  for (const dial of dialCodes) {
    if (cleaned.startsWith(dial)) {
      const mcc = DIAL_TO_MCC[dial];
      return { mcc, mnc: "", mccmnc: formatMccMnc(mcc, "") };
    }
  }
  return { mcc: "", mnc: "", mccmnc: "" };
}
